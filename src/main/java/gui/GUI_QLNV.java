    private void btnThemNhanVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemNhanVienActionPerformed
        NhanVien nv = new NhanVien();
        LichLamViec llv = new LichLamViec();
        llv.setMaCaLam((String) cboLichLamViec.getSelectedItem());
        nv.setHoTen(txtHoTen.getText());
        nv.setDiaChi(txtDiaChi.getText());
        nv.setEmail(txtEmail.getText());
        nv.setChucVu((String) cboChucVu.getSelectedItem());
        try {
            nv.setNgaySinh(new SimpleDateFormat("dd/MM/yyyy").parse(txtNgaySinh.getText()));
        } catch (Exception e) {
            e.printStackTrace();
        }
        nv.setGioiTinh((String) cboGioiTinh.getSelectedItem());
        nv.setSoDienThoai(txtSoDienThoai.getText());
        nv.setLichLamViec(llv);
        nv.setTaiKhoan(new TaiKhoan(txtTaiKhoan.getText(), txtPassword.getText(), nv));
        if (nhanviendao.addNhanVien(nv)) {
            xoaTrang();
            listNhanVien.add(nv);
            showTable();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm nhân viên thất bại");
        }
    }//GEN-LAST:event_btnThemNhanVienActionPerformed

    private void btnSuaNhanVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaNhanVienActionPerformed
         selectRow = tableDSNhanVien.getSelectedRow();
        if (listNhanVien.size() == 0) {
            JOptionPane.showMessageDialog(this, "Không có dữ liệu trong bảng nhân viên");
        } else if (selectRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn khách hàng cần sửa");
        } else {
            NhanVien nv = new NhanVien();
            nv.setMaNhanVien(listNhanVien.get(selectRow).getMaNhanVien());
            LichLamViec llv = new LichLamViec();
            llv.setMaCaLam((String) cboLichLamViec.getSelectedItem());
            nv.setHoTen(txtHoTen.getText());
            nv.setDiaChi(txtDiaChi.getText());
            nv.setEmail(txtEmail.getText());
            nv.setChucVu((String) cboChucVu.getSelectedItem());
            try {
                nv.setNgaySinh(new SimpleDateFormat("dd/MM/yyyy").parse(txtNgaySinh.getText()));
            } catch (Exception e) {
                e.printStackTrace();
            }
            nv.setGioiTinh((String) cboGioiTinh.getSelectedItem());
            nv.setSoDienThoai(txtSoDienThoai.getText());
            nv.setLichLamViec(llv);
            TaiKhoan tk = new TaiKhoan(txtTaiKhoan.getText(), txtPassword.getText(), nv);
            tk.setMaTaiKhoan(listNhanVien.get(selectRow).getTaiKhoan().getMaTaiKhoan());
            nv.setTaiKhoan(tk);
            if(nhanviendao.updateNhanVien(nv)){
                tableDSNhanVien.setValueAt(txtHoTen.getText(), selectRow, 1);
                tableDSNhanVien.setValueAt(txtDiaChi.getText(), selectRow, 2);
                tableDSNhanVien.setValueAt(txtNgaySinh.getText(), selectRow,3);
                tableDSNhanVien.setValueAt(txtSoDienThoai.getText(), selectRow, 4);
                tableDSNhanVien.setValueAt(cboGioiTinh.getSelectedItem(), selectRow, 5);
                tableDSNhanVien.setValueAt(txtEmail.getText(), selectRow, 6);
                tableDSNhanVien.setValueAt(cboChucVu.getSelectedItem(), selectRow, 7);
                tableDSNhanVien.setValueAt(cboLichLamViec.getSelectedItem(), selectRow, 8);
                xoaTrang();
                JOptionPane.showMessageDialog(this, "Cập nhật nhân viên thành công");
            }else{
                JOptionPane.showMessageDialog(this, "Cập nhật nhân viên thất bại");
            }
        }
    }//GEN-LAST:event_btnSuaNhanVienActionPerformed

    private void btnXoaNhanVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaNhanVienActionPerformed
        selectRow = tableDSNhanVien.getSelectedRow();
        if (selectRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn nhân viên cần xóa");
        } else {
            int maNhanVien = listNhanVien.get(selectRow).getMaNhanVien();
            if (nhanviendao.deleteNhanVien(maNhanVien)) {
                model.removeRow(selectRow);
                JOptionPane.showMessageDialog(this, "Xóa nhân viên thành công");
            } else {
                JOptionPane.showMessageDialog(this, "Xóa nhân viên thất bại");
            }
        }
    }//GEN-LAST:event_btnXoaNhanVienActionPerformed

