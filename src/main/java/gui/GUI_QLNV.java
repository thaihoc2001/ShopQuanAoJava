    private void showTable() {
        model.setRowCount(0);
        DateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");
        for (NhanVien nv : listNhanVien) {
            model.addRow(new Object[]{
                nv.getMaNhanVien(), nv.getHoTen(), nv.getDiaChi(), formatter.format(nv.getNgaySinh()), nv.getSoDienThoai(), nv.getGioiTinh(), nv.getEmail(), nv.getChucVu(), nv.getLichLamViec().getMaCaLam()
            });
        }
    }
     private void showComboBoxLichLamViec() {
        List<LichLamViec> listLichLamViec = lichLamViecDAO.getListLichLamViec();
        for (LichLamViec llv : listLichLamViec) {
            cboLichLamViec.addItem(llv.getMaCaLam());
        }
    }

    private void xoaTrang() {
        txtHoTen.setText("");
        txtDiaChi.setText("");
        txtNgaySinh.setText("");
        txtEmail.setText("");
        txtSoDienThoai.setText("");
        cboChucVu.setSelectedItem(null);
        cboGioiTinh.setSelectedItem(null);
        cboLichLamViec.setSelectedItem(null);
        txtTaiKhoan.setText("");
        txtPassword.setText("");
    }

    private void loadDataToField(NhanVien nv) {
        txtHoTen.setText(nv.getHoTen());
        txtDiaChi.setText(nv.getDiaChi());
        DateFormat sm = new SimpleDateFormat("dd/MM/yyyy");
        txtNgaySinh.setText(sm.format(nv.getNgaySinh()));
        txtEmail.setText(nv.getEmail());
        txtSoDienThoai.setText(nv.getSoDienThoai());
        cboChucVu.setSelectedItem(nv.getChucVu());
        cboGioiTinh.setSelectedItem(nv.getGioiTinh());
        cboLichLamViec.setSelectedItem(nv.getLichLamViec().getMaCaLam());
        txtTaiKhoan.setText(nv.getTaiKhoan().getTenDangNhap());
        txtPassword.setText(nv.getTaiKhoan().getMatKhau());
    }

